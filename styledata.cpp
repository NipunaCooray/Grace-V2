int StyleData::convertSpeedtoModular_pinch(int speed){

    int convertedSpeed=0;
    double dacValue = 0;
    double linearSpeed = 0;

     dacValue = speed/1000.0;

    if (dacValue >= 0.1 && dacValue <= 0.5){
        linearSpeed = 18.086 * dacValue - 0.0841;
    } else if (dacValue > 0.5 && dacValue <= 1.0){
        linearSpeed = 2.7116 * dacValue + 7.7112;
    }
      else {
        cout<< "Invalid DAC"<<endl;
    }


    float wheelRadius = 0.85;   // Radius of the wheel attached to the motor in cm

    double motorFrequency = linearSpeed / (2 * 3.1415926 * wheelRadius);

    double motorIntValue = motorFrequency * 60 * 64;

    convertedSpeed = motorIntValue;

    return convertedSpeed;
}

